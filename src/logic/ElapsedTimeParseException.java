package logic;

public class ElapsedTimeParseException extends Exception {
    public ElapsedTimeParseException() { super(); }
    public ElapsedTimeParseException(String message) { super(message); }
    public ElapsedTimeParseException(String message, Throwable cause) { super(message, cause); }
    public ElapsedTimeParseException(Throwable cause) { super(cause); }
}
