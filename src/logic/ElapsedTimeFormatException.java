package logic;

public class ElapsedTimeFormatException extends Exception {
    public ElapsedTimeFormatException() { super(); }
    public ElapsedTimeFormatException(String message) { super(message); }
    public ElapsedTimeFormatException(String message, Throwable cause) { super(message, cause); }
    public ElapsedTimeFormatException(Throwable cause) { super(cause); }
}
